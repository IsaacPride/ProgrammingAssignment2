## makeCacheMatrix returns a list of functions for
## setting and getting a matrix and its cached inverse.
## cacheSolve either returns the cached inverse of a
## makeCacheMatrix object, or, if there is no cached
## inverse, calculates, sets and returns it.

## Stores a matrix and its inverse

makeCacheMatrix <- function(x = matrix()) {
  i <- NULL
  set <- function(y) {
    x <<- y
    i <<- NULL
  }
  get <- function() x
  setinverse <- function(inverse) i <<- inverse
  getinverse <- function() i
  list(set = set, get = get,
       setinverse = setinverse,
       getinverse = getinverse)
}


## Returns inverse of matrix, using cached value if existent

cacheSolve <- function(x, ...) {
  i <- x$getinverse()
  if(!is.null(i)) {
    message("getting cached data")
    return(i)
  }
  data <- x$get()
  i <- solve(data, ...)
  x$setinverse(i)
  i
}
